var account = 3;

switch (account) {
    case 1:
        console.log("Checking account");   // => Checking account
        break;
    case 2:
        console.log("Savings account");    // => Savings account 
        break;
    case 3:
        console.log("Business account");   // => Business account 
        break;
    default:
        console.log("Invalid account code");
        break;
}